// One-time migration: existing hardcoded sketches → D1 seed + R2 upload script.
//
//   node migrations/import-sketches.mjs
//
// Produces:
//   migrations/seed-sketches.sql
//   migrations/upload-sketches.sh
//
// Images move from public/2d-images/<file> to R2 key sketches/<file>,
// served at /img/sketches/<file>.

import { writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const BUCKET = 'david-rejeski-images';

// Mirror of the original sketches.astro list.
const sketches = [
  { file: 'Bluesboys.jpeg', title: 'Bluesboys' },
  { file: 'CarlosWallPainting2.jpeg', title: 'Carlos Wall Painting' },
  { file: 'Castle.jpeg', title: 'Castle' },
  { file: 'Currywurststandfinal copy.jpeg', title: 'Currywurst Stand' },
  { file: 'DuBisteinBerliner2.jpeg', title: 'Du Bist ein Berliner' },
  { file: 'Fuckyourgas.jpeg', title: 'Fuck Your Gas' },
  { file: 'Klemke.jpg', title: 'Klemke' },
  { file: 'Litzensee2.jpeg', title: 'Litzensee' },
  { file: 'PotsdamRussianVillage2.jpg', title: 'Potsdam Russian Village' },
  { file: 'Putin.jpeg', title: 'Putin' },
  { file: 'Tefelsberg2.jpeg', title: 'Tefelsberg' },
  { file: 'cargobikes.jpg', title: 'Cargo Bikes' },
  { file: 'castlebridge.jpeg', title: 'Castle Bridge' },
  { file: 'killingart2.jpeg', title: 'Killing Art' },
  { file: 'moreespresso.jpeg', title: 'More Espresso' },
  { file: 'parking.jpeg', title: 'Parking' },
  { file: 'platzinsnow2.jpg', title: 'Platz in Snow' },
  { file: 'square1.jpeg', title: 'Square' },
];

const sqlStr = (v) => `'${String(v).replace(/'/g, "''")}'`;

const inserts = sketches.map(
  (s) =>
    `INSERT INTO sketches (title, image, sort_order) VALUES (${sqlStr(s.title)}, ${sqlStr(
      '/img/sketches/' + s.file,
    )}, ${Math.random().toFixed(6)});`,
);

writeFileSync(
  join(here, 'seed-sketches.sql'),
  ['-- Generated by import-sketches.mjs', 'DELETE FROM sketches;', ...inserts, ''].join('\n'),
);

const sh = [
  '#!/usr/bin/env bash',
  'set -euo pipefail',
  'cd "$(dirname "$0")/.."',
  ...sketches.map(
    (s) =>
      `npx wrangler r2 object put "${BUCKET}/sketches/${s.file}" --file="public/2d-images/${s.file}" --remote`,
  ),
  '',
].join('\n');
writeFileSync(join(here, 'upload-sketches.sh'), sh);

console.log(`Wrote ${inserts.length} sketches to migrations/seed-sketches.sql`);
console.log(`Wrote ${sketches.length} uploads to migrations/upload-sketches.sh`);
