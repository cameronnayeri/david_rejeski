// One-time migration: markdown pieces → D1 seed + R2 image upload script.
//
//   node migrations/import.mjs
//
// Produces:
//   migrations/seed.sql        — INSERT statements for the projects table
//   migrations/upload-images.sh — wrangler commands to push images into R2
//
// Then run:
//   npx wrangler d1 execute david-rejeski --remote --file=migrations/seed.sql
//   bash migrations/upload-images.sh
//
// Image paths are rewritten from /images/<slug>/<file> to /img/<slug>/<file>
// (served by src/pages/img/[...key].ts out of R2).

import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const siteRoot = join(here, '..');
const piecesDir = join(siteRoot, 'src/content/pieces');
const publicDir = join(siteRoot, 'public');
const BUCKET = 'david-rejeski-images';

function parseFrontmatter(raw) {
  const m = raw.match(/^---\n([\s\S]*?)\n---/);
  if (!m) return {};
  const out = {};
  const lines = m[1].split('\n');
  let key = null;
  let arr = null;
  for (const line of lines) {
    if (/^\s+-\s/.test(line) && arr) {
      arr.push(stripQuotes(line.replace(/^\s+-\s*/, '').trim()));
      continue;
    }
    const kv = line.match(/^([a-zA-Z_]+):\s*(.*)$/);
    if (!kv) continue;
    if (arr) { out[key] = arr; arr = null; }
    key = kv[1];
    const val = kv[2].trim();
    if (val === '') { arr = []; continue; } // block list follows
    if (val.startsWith('[')) { out[key] = parseInlineArray(val); continue; }
    out[key] = stripQuotes(val);
  }
  if (arr) out[key] = arr;
  return out;
}

function parseInlineArray(s) {
  const inner = s.replace(/^\[|\]$/g, '');
  if (!inner.trim()) return [];
  const items = [];
  let cur = '';
  let inStr = false;
  for (let i = 0; i < inner.length; i++) {
    const c = inner[i];
    if (c === '"' && inner[i - 1] !== '\\') inStr = !inStr;
    else if (c === ',' && !inStr) { items.push(cur); cur = ''; continue; }
    cur += c;
  }
  if (cur.trim()) items.push(cur);
  return items.map((x) => stripQuotes(x.trim()).replace(/\\"/g, '"'));
}

function stripQuotes(s) {
  return s.replace(/^["']|["']$/g, '');
}

function sqlStr(v) {
  if (v === null || v === undefined || v === '') return 'NULL';
  return `'${String(v).replace(/'/g, "''")}'`;
}

const files = readdirSync(piecesDir).filter((f) => f.endsWith('.md') && f !== 'testing.md');
const inserts = [];
const uploads = new Set();

for (const file of files) {
  const raw = readFileSync(join(piecesDir, file), 'utf8');
  const fm = parseFrontmatter(raw);
  const slug = file.replace(/\.md$/, '');

  const oldImages = Array.isArray(fm.images) ? fm.images : [];
  const newImages = oldImages.map((p) => p.replace(/^\/images\//, '/img/'));

  for (const img of oldImages) {
    const rel = img.replace(/^\//, ''); // images/slug/file
    const key = rel.replace(/^images\//, ''); // slug/file
    uploads.add(`${rel}\t${key}`);
  }

  const materials = Array.isArray(fm.materials) ? fm.materials : [];

  inserts.push(
    `INSERT INTO projects (title, slug, number, year, category, materials, description, caption, images, featured, status, sort_order) VALUES (` +
      [
        sqlStr(fm.title),
        sqlStr(slug),
        fm.number ?? 'NULL',
        fm.year ?? 'NULL',
        sqlStr(fm.category || 'Explorations'),
        sqlStr(JSON.stringify(materials)),
        sqlStr(fm.description),
        sqlStr(fm.caption),
        sqlStr(JSON.stringify(newImages)),
        fm.featured === 'true' ? 1 : 0,
        sqlStr(fm.status || 'published'),
        Math.random().toFixed(6),
      ].join(', ') +
      `);`,
  );
}

const seed = ['-- Generated by import.mjs', 'DELETE FROM projects;', ...inserts, ''].join('\n');
writeFileSync(join(here, 'seed.sql'), seed);

const sh = [
  '#!/usr/bin/env bash',
  'set -euo pipefail',
  'cd "$(dirname "$0")/.."',
  '# Uploads every project image into R2.',
  ...[...uploads].map((line) => {
    const [rel, key] = line.split('\t');
    return `npx wrangler r2 object put ${BUCKET}/${key} --file="public/${rel}" --remote`;
  }),
  '',
].join('\n');
writeFileSync(join(here, 'upload-images.sh'), sh);

console.log(`Wrote ${inserts.length} projects to migrations/seed.sql`);
console.log(`Wrote ${uploads.size} image uploads to migrations/upload-images.sh`);
